package com.gwideal.core.workflow;

public class DFormDefInfo {

	private String formDefId;
	private String formTable;
	private String formEntity;
	
	
	public String getFormDefId() {
		return formDefId;
	}
	public void setFormDefId(String formDefId) {
		this.formDefId = formDefId;
	}
	public String getFormTable() {
		return formTable;
	}
	public void setFormTable(String formTable) {
		this.formTable = formTable;
	}
	public String getFormEntity() {
		return formEntity;
	}
	public void setFormEntity(String formEntity) {
		this.formEntity = formEntity;
	}
	
	
	
	
}
